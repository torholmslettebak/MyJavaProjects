package myTowerDefence;

public class Mob 
{
	private int speed;
	private int health;
	private int armor;
	private int bounty;
	
	
	public int getSpeed() 
	{
		return speed;
	}
	public void setSpeed(int speed) 
	{
		this.speed = speed;
	}
	public int getHealth() 
	{
		return health;
	}
	public void setHealth(int health) 
	{
		this.health = health;
	}
	public int getArmor() 
	{
		return armor;
	}
	public void setArmor(int armor) 
	{
		this.armor = armor;
	}
	public int getBounty() 
	{
		return bounty;
	}
	public void setBounty(int bounty) 
	{
		this.bounty = bounty;
	}
}
